        "import yfinance as yf\n",
        "import pandas as pd\n",
        "import requests\n",
        "from tqdm import tqdm\n",
        "import warnings\n",
        "import logging\n",
        "\n",
        "# 1. Setup: Silence warnings and yfinance logs\n",
        "warnings.filterwarnings(\"ignore\", category=FutureWarning)\n",
        "logging.getLogger('yfinance').setLevel(logging.CRITICAL)\n",
        "\n",
        "def get_sp500_tickers():\n",
        "    \"\"\"Fetches and cleans S&P 500 tickers from Wikipedia.\"\"\"\n",
        "    url = 'https://en.wikipedia.org/wiki/List_of_S%26P_500_companies'\n",
        "    headers = {'User-Agent': 'Mozilla/5.0'}\n",
        "\n",
        "    try:\n",
        "        response = requests.get(url, headers=headers)\n",
        "        # Force pandas to read the first table\n",
        "        df = pd.read_html(response.text)[0]\n",
        "\n",
        "        # Take the first column, convert to string\n",
        "        raw_list = df.iloc[:, 0].astype(str).tolist()\n",
        "\n",
        "        clean_tickers = []\n",
        "        for t in raw_list:\n",
        "            t = t.strip().upper().replace('.', '-')\n",
        "            # Filter: must have letters, no '---', and reasonable length\n",
        "            if t.isalpha() or ('-' in t and t.replace('-', '').isalpha()):\n",
        "                if len(t) < 7 and t != 'SYMBOL':\n",
        "                    clean_tickers.append(t)\n",
        "\n",
        "        print(f\"‚úÖ Successfully fetched {len(clean_tickers)} tickers.\")\n",
        "        print(f\"üìå Sample: {clean_tickers[:10]}\")\n",
        "        return clean_tickers\n",
        "    except Exception as e:\n",
        "        print(f\"‚ùå Error fetching tickers: {e}\")\n",
        "        return []\n",
        "\n",
        "def run_scanner():\n",
        "    tickers = get_sp500_tickers()\n",
        "    if not tickers:\n",
        "        return pd.DataFrame()\n",
        "\n",
        "    bullish_stocks = []\n",
        "    print(f\"\\nScanning stocks for Price > Prev Day IB High AND Price > Today ON High...\")\n",
        "\n",
        "    for ticker in tqdm(tickers):\n",
        "        try:\n",
        "            # Download 3 days to ensure we have 'Yesterday' and 'Today'\n",
        "            data = yf.download(ticker, period=\"3d\", interval=\"5m\", prepost=True, progress=False, auto_adjust=True)\n",
        "\n",
        "            if data.empty or len(data) < 20:\n",
        "                continue\n",
        "\n",
        "            # Flatten Multi-index columns if they exist\n",
        "            if isinstance(data.columns, pd.MultiIndex):\n",
        "                data.columns = data.columns.get_level_values(0)\n",
        "\n",
        "            # Convert to Chicago Time\n",
        "            data.index = data.index.tz_convert('America/Chicago')\n",
        "\n",
        "            # Identify the unique trading days\n",
        "            days = sorted(data.index.normalize().unique())\n",
        "            if len(days) < 2:\n",
        "                continue\n",
        "\n",
        "            yesterday = days[-2] # Previous trading day\n",
        "            today = days[-1]     # Current trading day\n",
        "\n",
        "            # --- LOGIC 1: PREVIOUS DAY INITIAL BALANCE HIGH (08:30 - 09:30 CT) ---\n",
        "            prev_day_data = data.loc[yesterday.strftime('%Y-%m-%d')]\n",
        "            ib_data = prev_day_data.between_time('08:30', '09:30')\n",
        "            if ib_data.empty: continue\n",
        "            prev_ib_high = ib_data['High'].max()\n",
        "\n",
        "            # --- LOGIC 2: TODAY'S OVERNIGHT HIGH (00:00 - 08:30 CT) ---\n",
        "            today_data = data.loc[today.strftime('%Y-%m-%d')]\n",
        "            on_data = today_data.between_time('00:00', '08:30')\n",
        "            if on_data.empty: continue\n",
        "            on_high = on_data['High'].max()\n",
        "\n",
        "            # --- LOGIC 3: CURRENT PRICE ---\n",
        "            current_price = today_data['Close'].iloc[-1]\n",
        "            on_high = today_data.between_time('00:00', '08:30')['High'].max()\n",
        "            # --- FINAL FILTER ---\n",
        "            if current_price > prev_ib_high and current_price > on_high:\n",
        "                bullish_stocks.append({\n",
        "                    \"Ticker\": ticker,\n",
        "                    \"Pivot Price\":on_high,\n",
        "                    \"Gap_Up_%\": round(((float(current_price) / float(prev_ib_high)) - 1) * 100, 2)\n",
        "                })\n",
        "\n",
        "        except Exception:\n",
        "            continue\n",
        "\n",
        "    return pd.DataFrame(bullish_stocks)\n",
        "\n",
        "# EXECUTE SCRIPT\n",
        "results_df = run_scanner()\n",
        "\n",
        "if not results_df.empty:\n",
        "    print(\"\\nüöÄ BULLISH BREAKOUTS FOUND:\")\n",
        "    # Sort by the ones showing most strength (highest Gap Up %)\n",
        "    display(results_df.sort_values(by=\"Gap_Up_%\", ascending=False))\n",
        "else:\n",
        "    print(\"\\nüò¥ No stocks currently meet the breakout criteria.\")\n",
        "\n"
